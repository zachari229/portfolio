/**
 * Applique la langue choisie à tous les éléments [data-i18n]
 * @param {string} lang - Code langue : "fr" | "en" | "es"
 */
function applyTranslation(lang) {
  const dict = translations[lang];
  if (!dict) return;

  // 1. Traduire tous les éléments data-i18n
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // 1b. Traduire les placeholders des champs de formulaire
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      el.setAttribute("placeholder", dict[key]);
    }
  });

  // 2. Mettre à jour le <title> de la page
  if (dict.site_title) {
    document.title = dict.site_title;
  }

  // 3. Mettre à jour la balise <meta name="description">
  const metaDesc = document.getElementById("meta_description");
  if (metaDesc && dict.meta_description) {
    metaDesc.setAttribute("content", dict.meta_description);
  }

  // 4. Mettre à jour l'attribut lang de <html>
  document.documentElement.lang = lang;

  // 5. Sauvegarder la langue choisie
  localStorage.setItem("lang", lang);

  // 6. Relancer Typed.js avec les nouveaux textes si disponibles
  if (dict.typed_strings && window.typedInstance) {
    window.typedInstance.destroy();
    window.typedInstance = new Typed("#textDefil", {
      strings: dict.typed_strings,
      typeSpeed: 60,
      backSpeed: 40,
      loop: true
    });
  }
}

/**
 * Initialise le système de traduction.
 * À appeler APRÈS que le header soit injecté dans le DOM.
 */
function initTranslations() {
  const savedLang = localStorage.getItem("lang") || "fr";
  const select = document.getElementById("lang");

  if (select) {
    // Appliquer la langue sauvegardée au sélecteur
    select.value = savedLang;

    // Écouter les changements de langue
    select.addEventListener("change", function () {
      applyTranslation(this.value);
    });

    document.dispatchEvent(new CustomEvent('langChanged', {
        detail: { lang: this.value }
      }));
  }

  // Appliquer la traduction au chargement
  applyTranslation(savedLang);
}